import time 
import os 
import sys
def create_ascii_animation(frames,delay_time):
    for frame in frames:
        #Flushing the output buffer,so that the text appears as soon as possible 
        sys.stdout.flush()
        time.sleep(delay_time)
        os.system('cls')
        print(frame)
